module.exports = function(){
  var scope = new at.Scope('global');
  var globalToken = ['object', []];
  globalToken.obj = scope.names;

  scope.put('global', 'env', globalToken);
  scope.put('this', 'env', globalToken);
  scope.put('window', 'env', globalToken);

  []
    .concat(ES5literals)
    .concat(browserLiterals)
    .forEach(function(name){
      scope.put(name, 'env', ['object', []]);
    });

  []
    .concat(ES5)
    .concat(ES6)
    .concat(browser)
    .forEach(function(name){
      scope.put(name, 'env', ['function', null, [], []]);
    });

  return scope;
};
